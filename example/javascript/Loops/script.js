//Loop types

//for
for(let i=0;i<10;i++){
    console.log(i);
}

//forEach
let list = ["eat", "sleep", "program", "walk"];
list.forEach((item) => {
    console.log(item);
})

//while
let i=0;
while(i<10){
    console.log(i);
    i++;
}

//do while
i=0;
do{
    console.log(i);
    i++;
}while(i<10);